(function () {
  const SPEED = 55; // pixels per second

  const wire = document.getElementById("x286-newswire");
  const track = wire && wire.querySelector(".x286-nw-track");
  if (!wire || !track) return;

  // Shuffle so each session runs a different order
  const shuffled = [...X286_HEADLINES].sort(() => Math.random() - 0.5);
  const text = shuffled.join("   \u25C6   ") + "   \u25C6   ";

  // Two identical spans end-to-end for a seamless loop
  [0, 1].forEach(() => {
    const span = document.createElement("span");
    span.textContent = text;
    track.appendChild(span);
  });

  let xOffset = 0;
  let lastTs = null;
  let segWidth = 0;

  function tick(ts) {
    // Re-measure every frame until we get a real width.
    // offsetWidth is 0 while the element is hidden (display:none),
    // so this handles switching into x286 theme after page load.
    if (segWidth === 0) {
      segWidth = track.firstElementChild.offsetWidth;
    }

    const dt = lastTs ? (ts - lastTs) / 1000 : 0;
    lastTs = ts;

    if (segWidth > 0) {
      xOffset -= SPEED * dt;
      if (xOffset <= -segWidth) xOffset += segWidth;
      track.style.transform = `translateX(${Math.round(xOffset)}px)`;
    }

    requestAnimationFrame(tick);
  }

  requestAnimationFrame(tick);
}());
